/**
 * Benchmark to measure the size of the Java code generated by whole-stage codegen for the TPC-DS
 * queries. Unlike [[TPCDSQueryBenchmark]], this does not execute the queries and needs no data: it
 * plans each query over empty TPC-DS tables, collects the whole-stage-codegen subtrees, and reports
 * how large the generated (and compiled) code is. It is the standard instrument for changes that aim
 * to reduce generated Java size (see the umbrella SPARK-56908).
 *
 * The reported grand totals (lower is better, except the query/stage counts) are:
 *   - source code size: characters of generated Java summed over all stages;
 *   - max method bytecode (sum): the per-stage largest compiled method size, summed over stages --
 *     this is the metric behind the HotSpot 8KB JIT-compilation threshold and the 64KB method limit;
 *   - max method bytecode (max): the single largest compiled method across all stages;
 *   - inner classes: number of generated inner classes summed over all stages;
 *   - constant pool (sum / max): compiled constant-pool entries, summed and the per-stage max;
 *   - compile time: total Janino compilation time (ms), measured cold via
 *     [[CodeGenerator.resetCompileTime()]];
 *   - codegen fallbacks: stages whose generated code failed to compile (fell back to interpreted);
 *   - stages / queries: the whole-stage-codegen stage count and the number of queries measured.
 *
 * To run this benchmark:
 * {{{
 *   1. without sbt:
 *        bin/spark-submit --jars <spark core test jar>,<spark catalyst test jar>
 *          --class <this class> <spark sql test jar>
 *   2. build/sbt "sql/Test/runMain <this class>"
 *   3. generate result:
 *        SPARK_GENERATE_BENCHMARK_FILES=1 build/sbt "sql/Test/runMain <this class>"
 *      Results will be written to
 *      "sql/core/benchmarks/WholeStageCodegenSizeBenchmark-results.txt".
 * }}}
 */
object WholeStageCodegenSizeBenchmark extends SqlBasedBenchmark with Logging {

  // Inject SF=100 table stats so join/aggregate strategy selection (and therefore the shape of the
  // generated code) resembles planning on real data, matching PlanStabilitySuite's sf100 plans.
  private val injectStats = true

  override def getSparkSession: SparkSession = {
    val conf = new SparkConf()
      .setMaster(System.getProperty("spark.sql.test.master", "local[1]"))
      .setAppName(this.getClass.getCanonicalName)
      .set(SQLConf.WHOLESTAGE_CODEGEN_ENABLED.key, "true")
      // Keep the codegen fallback path so we can count stages that fail to compile, instead of
      // failing the benchmark.
      .set(SQLConf.CODEGEN_FALLBACK.key, "true")
      // Disable AQE so the physical plan (and its whole-stage-codegen stages) is fully materialized
      // at planning time, without needing to execute the query to resolve query stages.
      .set(SQLConf.ADAPTIVE_EXECUTION_ENABLED.key, "false")
      .set(SQLConf.SHUFFLE_PARTITIONS.key, "4")
      .set("spark.sql.crossJoin.enabled", "true")
      .set("spark.sql.sources.default", "parquet")

    val spark = SparkSession.builder().config(conf).getOrCreate()
    if (injectStats) {
      // Stats-based strategies (broadcast vs shuffle join, etc.) need the CBO on to be applied.
      spark.conf.set(SQLConf.CBO_ENABLED.key, "true")
      spark.conf.set(SQLConf.PLAN_STATS_ENABLED.key, "true")
      spark.conf.set(SQLConf.JOIN_REORDER_ENABLED.key, "true")
    }
    spark
  }

  /** Grand-total accumulators across all measured stages of all queries. */
  private case class Totals(
      var queries: Int = 0,
      var stages: Int = 0,
      var sourceChars: Long = 0L,
      var maxMethodBytecodeSum: Long = 0L,
      var maxMethodBytecodeMax: Long = 0L,
      var innerClasses: Long = 0L,
      var constPoolSum: Long = 0L,
      var constPoolMax: Long = 0L,
      var fallbacks: Int = 0)

  private def createTables(): Unit = {
    CodegenSizeTpcdsSchema.tableNames.foreach { tableName =>
      spark.sql(
        s"""
           |CREATE TABLE `$tableName` (${CodegenSizeTpcdsSchema.columnsDDL(tableName)})
           |USING parquet
           |${CodegenSizeTpcdsSchema.partitionedByClause(tableName)}
         """.stripMargin)
      if (injectStats) {
        spark.sessionState.catalog.alterTableStats(
          TableIdentifier(tableName), Some(TPCDSTableStats.sf100TableStats(tableName)))
      }
    }
  }

  private def dropTables(): Unit = {
    CodegenSizeTpcdsSchema.tableNames.foreach { tableName =>
      spark.sessionState.catalog.dropTable(TableIdentifier(tableName), ignoreIfNotExists = true,
        purge = true)
    }
  }

  /** Plans one query and folds its whole-stage-codegen size metrics into `totals`. */
  private def measureQuery(queryLocation: String, name: String, totals: Totals): Unit = {
    val queryString = resourceToString(s"$queryLocation/$name.sql",
      classLoader = Thread.currentThread().getContextClassLoader)
    try {
      val plan = spark.sql(queryString).queryExecution.executedPlan
      // codegenStringSeq walks all WholeStageCodegenExec subtrees (recursing subqueries), calls
      // doCodeGen() for the source and CodeGenerator.compile() for the compiled ByteCodeStats.
      val stages = codegenStringSeq(plan)
      totals.queries += 1
      stages.foreach { case (_, source, stats) =>
        totals.stages += 1
        totals.sourceChars += source.length
        if (stats == ByteCodeStats.UNAVAILABLE) {
          totals.fallbacks += 1
        } else {
          totals.maxMethodBytecodeSum += stats.maxMethodCodeSize
          totals.maxMethodBytecodeMax =
            math.max(totals.maxMethodBytecodeMax, stats.maxMethodCodeSize)
          totals.innerClasses += stats.numInnerClasses
          totals.constPoolSum += stats.maxConstPoolSize
          totals.constPoolMax = math.max(totals.constPoolMax, stats.maxConstPoolSize)
        }
      }
    } catch {
      case NonFatal(e) =>
        logWarning(s"Skipping query $name: failed to plan (${e.getMessage})")
    }
  }

  override def runBenchmarkSuite(mainArgs: Array[String]): Unit = {
    createTables()
    try {
      val queriesV1_4 = CodegenSizeTpcdsSchema.tpcdsQueries
      val queriesV2_7 = CodegenSizeTpcdsSchema.tpcdsQueriesV2_7_0

      // Measure compile time cold: reset the accumulator, let codegenStringSeq trigger the Janino
      // compiles (the compile cache is keyed on the source string, so distinct queries miss), then
      // read the total below.
      CodeGenerator.resetCompileTime()
      val totals = Totals()
      queriesV1_4.foreach(measureQuery("tpcds", _, totals))
      queriesV2_7.foreach(measureQuery("tpcds-v2.7.0", _, totals))
      val compileTimeMs = CodeGenerator.compileTime / 1000000.0

      runBenchmark("TPCDS codegen size") {
        // Use a Benchmark instance only for its `out` PrintStream, which tees to stdout and the
        // results file; the metrics here are static sizes, not wall-clock timings.
        val benchmark = new Benchmark("TPCDS codegen size (grand totals, lower is better)",
          totals.queries.toLong, output = output)
        val out = benchmark.out
        // scalastyle:off println
        def row(label: String, value: Any): Unit =
          out.println("%-32s %s".format(label, value))
        row("queries measured", totals.queries)
        row("codegen stages", totals.stages)
        row("source code size (chars)", totals.sourceChars)
        row("max method bytecode (sum)", totals.maxMethodBytecodeSum)
        row("max method bytecode (max)", totals.maxMethodBytecodeMax)
        row("inner classes", totals.innerClasses)
        row("constant pool (sum)", totals.constPoolSum)
        row("constant pool (max)", totals.constPoolMax)
        row("codegen fallbacks", totals.fallbacks)
        row("compile time (ms)", "%.1f".format(compileTimeMs))
        out.println()
        // scalastyle:on println
      }
    } finally {
      dropTables()
    }
  }
}

/**
