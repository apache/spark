package org.apache.spark.sql.execution.datasources.orc

import java.time.LocalTime

import org.apache.spark.sql.QueryTest
import org.apache.spark.sql.test.SharedSparkSession
import org.apache.spark.sql.types.TimeType

class OrcTimeTypeSuite extends QueryTest with SharedSparkSession {
  import testImplicits._

  test("ORC: Basic Read/Write Test") {
    withTempPath { path =>
      val df = Seq("10:15:30").toDF("t").selectExpr("CAST(t AS TIME)")
      df.write.orc(path.getCanonicalPath)
      val read = spark.read.orc(path.getCanonicalPath)
      checkAnswer(read, df)
    }
  }

  test("ORC: Round-trip correctness") {
    withTempPath { path =>
      val data = Seq(
        LocalTime.of(10, 15, 30, 123456000),
        LocalTime.of(0, 0, 0, 0),
        LocalTime.of(23, 59, 59, 999000000) // micros precision
      )
      val df = data.map(Tuple1(_)).toDF("t")
      df.write.orc(path.getCanonicalPath)
      val read = spark.read.orc(path.getCanonicalPath)
      checkAnswer(read, df)
    }
  }

  test("ORC: Edge Values") {
    withTempPath { path =>
      val df = spark.sql("SELECT TIME '00:00:00' as t1, TIME '23:59:59.999999' as t2")
      df.write.orc(path.getCanonicalPath)
      val read = spark.read.orc(path.getCanonicalPath)
      checkAnswer(read, df)
    }
  }

  test("ORC: Null Handling") {
    withTempPath { path =>
      val data = Seq(Some(LocalTime.of(12, 0)), None)
      val df = data.map(Tuple1(_)).toDF("t")
      df.write.orc(path.getCanonicalPath)
      val read = spark.read.orc(path.getCanonicalPath)
      checkAnswer(read, df)
    }
  }

  test("ORC: Schema Verification") {
    withTempPath { path =>
      val df = Seq(LocalTime.of(12, 0)).map(Tuple1(_)).toDF("t")
      df.write.orc(path.getCanonicalPath)
      val schema = spark.read.orc(path.getCanonicalPath).schema
      assert(schema("t").dataType == TimeType)
    }
  }

  test("ORC: Mixed Types") {
    withTempPath { path =>
      val df = spark.sql("""
        SELECT
          TIME '12:00:00' as t,
          TIMESTAMP '2023-01-01 12:00:00' as ts,
          DATE '2023-01-01' as d
      """)
      df.write.orc(path.getCanonicalPath)
      val read = spark.read.orc(path.getCanonicalPath)
      checkAnswer(read, df)
    }
  }

  test("ORC: Vectorized Read") {
    val data = (0 to 23).map(h => LocalTime.of(h, 0, 0))
    val df = data.map(Tuple1(_)).toDF("t")
    withTempPath { path =>
      df.write.orc(path.getCanonicalPath)

      withSQLConf("spark.sql.orc.enableVectorizedReader" -> "true") {
        val vectorized = spark.read.orc(path.getCanonicalPath)
        checkAnswer(vectorized, df)
      }

      withSQLConf("spark.sql.orc.enableVectorizedReader" -> "false") {
        val nonVectorized = spark.read.orc(path.getCanonicalPath)
        checkAnswer(nonVectorized, df)
      }
    }
  }

  test("ORC: Compatibility Test") {
    // This tests that we can read back what we wrote, which is the basic compatibility.
    // In a real scenario, we might want to test reading ORC files generated by older
    // Spark versions, but since TimeType is new, there are no "old" ORC files with
    // TimeType. We can test that a Long column in ORC can be read as TimeType if
    // schema is provided.
    withTempPath { path =>
      val df = Seq(123456789L).toDF("t")
      df.write.orc(path.getCanonicalPath)

      val read = spark.read.schema("t TIME").orc(path.getCanonicalPath)
      // 123456789 micros is 123 seconds and 456,789 micros
      // 123s = 2m 3s
      val expectedTime = LocalTime.of(0, 2, 3, 456789000)
      checkAnswer(read, Seq(expectedTime).map(Tuple1(_)).toDF())
    }
  }
}
