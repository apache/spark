package org.apache.spark.sql.execution.datasources.v2

import scala.jdk.CollectionConverters._

import org.apache.spark.sql.catalyst.{InternalRow, TableIdentifier}
import org.apache.spark.sql.catalyst.analysis.{ViewAlreadyExistsException, ViewSchemaMode}
import org.apache.spark.sql.catalyst.expressions.Attribute
import org.apache.spark.sql.catalyst.plans.logical.LogicalPlan
import org.apache.spark.sql.catalyst.catalog.CatalogTable.VIEW_SQL_CONFIG_PREFIX
import org.apache.spark.sql.connector.catalog.{Identifier, ViewCatalog, ViewInfo}
import org.apache.spark.sql.execution.command.ViewHelper
import org.apache.spark.sql.types.StructType

/**
 * Physical plan node for CREATE [OR REPLACE] VIEW targeting a V2 [[ViewCatalog]].
 *
 * The [[ViewCatalog.createView]] / [[ViewCatalog.replaceView]] API is called with a [[ViewInfo]]
 * that captures the view SQL text, resolved schema, and user-specified properties.
 *
 * @param catalog          the target [[ViewCatalog]]
 * @param ident            the target view identifier
 * @param viewSchema       the output schema of the view after applying column aliases
 * @param queryColumnNames the raw output column names of the defining query
 * @param columnAliases    user-specified column aliases (empty if none specified)
 * @param columnComments   user-specified per-column comments (null entries where absent)
 * @param comment          optional view-level comment
 * @param properties       user-specified TBLPROPERTIES
 * @param originalText     the original SQL text of the view definition
 * @param analyzedQuery    the analyzed logical plan of the view body (used for validation)
 * @param allowExisting    if true and the view already exists, the command is a no-op
 * @param replace          if true, replace an existing view (CREATE OR REPLACE semantics)
 * @param viewSchemaMode   schema binding mode (UNSUPPORTED / BINDING / EVOLUTION)
 * @param currentCatalog   the current catalog name at view-creation time
 * @param currentNamespace the current namespace at view-creation time
 */
case class CreateViewExec(
    catalog: ViewCatalog,
    ident: Identifier,
    viewSchema: StructType,
    queryColumnNames: Array[String],
    columnAliases: Array[String],
    columnComments: Array[String],
    comment: Option[String],
    properties: Map[String, String],
    originalText: String,
    analyzedQuery: LogicalPlan,
    allowExisting: Boolean,
    replace: Boolean,
    viewSchemaMode: ViewSchemaMode,
    currentCatalog: String,
    currentNamespace: Array[String]) extends LeafV2CommandExec {

  override def output: Seq[Attribute] = Seq.empty

  override protected def run(): Seq[InternalRow] = {
    // Construct a TableIdentifier for use in validation error messages.
    val v1Name = TableIdentifier(
      ident.name(),
      if (ident.namespace().nonEmpty) Some(ident.namespace().mkString(".")) else None)

    // Permanent V2 views cannot reference temporary objects.
    // Note: referredTempFunctions is not available at physical-plan time; temp function
    // references are validated in the V1 path via AnalysisContext. TODO: propagate
    // referredTempFunctions from the analysis phase for V2 views (SPARK-33903 follow-up).
    ViewHelper.verifyTemporaryObjectsNotExists(
      isTemporary = false, v1Name, analyzedQuery, referredTempFunctions = Seq.empty)
    ViewHelper.verifyAutoGeneratedAliasesNotExists(
      analyzedQuery, isTemporary = false, v1Name)

    // Capture the active SQL configs at view-creation time so that the view is always resolved
    // with the same settings (e.g. ANSI mode, session timezone) regardless of the session config
    // in effect when the view is later queried.  This mirrors what V1 does via generateViewProperties.
    // TODO (SPARK-33903 follow-up): referred temp object names are not stored because
    // referredTempFunctionNames is only available in AnalysisContext during analysis and is
    // not propagated to the physical plan.  Fixing this requires a new intermediate logical node
    // that extends AnalysisOnlyCommand (similar to how CreateViewCommand works in the V1 path).
    val finalProperties =
      comment.map(ViewCatalog.PROP_COMMENT -> _).toMap ++
        properties ++
        ViewHelper.sqlConfigsToProps(conf, VIEW_SQL_CONFIG_PREFIX) ++
        ViewHelper.viewSchemaModeToProps(viewSchemaMode)

    val viewInfo = new ViewInfo(
      ident,
      originalText,
      currentCatalog,
      currentNamespace,
      viewSchema,
      queryColumnNames,
      columnAliases,
      columnComments,
      finalProperties.asJava)

    if (replace) {
      // orCreate = true: create the view if it does not exist yet
      catalog.replaceView(viewInfo, true)
    } else {
      try {
        catalog.createView(viewInfo)
      } catch {
        case _: ViewAlreadyExistsException if allowExisting => // no-op for IF NOT EXISTS
      }
    }

    Seq.empty
  }
}
