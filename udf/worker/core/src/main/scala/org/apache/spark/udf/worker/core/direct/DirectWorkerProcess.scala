package org.apache.spark.udf.worker.core.direct

import java.io.File
import java.nio.file.{Files, Path}
import java.util.concurrent.TimeUnit
import java.util.concurrent.atomic.{AtomicBoolean, AtomicInteger}

import scala.util.control.NonFatal

import org.apache.spark.annotation.Experimental
import org.apache.spark.udf.worker.core.{WorkerConnection, WorkerLogger}

/**
 * :: Experimental ::
 * A locally-spawned OS process running a UDF worker, together with the
 * transport connection to it.
 *
 * A [[DirectWorkerProcess]] combines three things:
 *  - An OS '''process''' (the worker binary, started by the dispatcher),
 *    along with its socket file and output log -- all owned jointly by a
 *    [[WorkerArtifacts]] bundle.
 *  - A '''[[WorkerConnection]]''' (the transport channel to that process).
 *  - A session '''ref-count''' for future pooling.
 *
 * The dispatcher currently creates one process per session and tears it
 * down when the session closes; the [[acquireSession]]/[[releaseSession]]
 * ref-count is scaffolding for future pooling, where a single process
 * will back multiple concurrent sessions.
 *
 * Closing this wrapper closes the connection, attempts a graceful
 * SIGTERM, then delegates the forced kill + file cleanup to
 * [[WorkerArtifacts.close]].
 *
 * @param id                stable identifier for this worker (a UUID
 *                          generated by the dispatcher and passed to the
 *                          worker binary as `--id`). Used as the map key
 *                          in the dispatcher's worker registry and in
 *                          diagnostic messages.
 * @param artifacts         owns the OS process, socket file, and output
 *                          log. The same bundle is used for spawn-failure
 *                          teardown before this wrapper exists, so the
 *                          dispose logic is centralised there.
 * @param connection        the transport connection to this worker.
 * @param gracefulTimeoutMs milliseconds to wait after SIGTERM before
 *                          escalating (via `artifacts.close`) to SIGKILL.
 * @param logger            [[WorkerLogger]] used for process-level
 *                          messages. Defaults to [[WorkerLogger.NoOp]];
 *                          the dispatcher normally passes its own
 *                          logger so all messages share a category.
 * @param onLastSessionReleased
 *                          callback fired when the session ref-count
 *                          transitions to 0. The dispatcher wires this to
 *                          its reuse policy: today, terminate the worker;
 *                          with pooling, return it to an idle pool or
 *                          evict it. Runs on the thread that calls
 *                          [[releaseSession]] (typically the session-close
 *                          thread). May fire multiple times across a
 *                          worker's lifetime -- once per 0-transition --
 *                          and runs without holding the count at 0: a
 *                          concurrent `acquireSession` can push the count
 *                          back up before the callback returns, so a
 *                          pooling dispatcher must arbitrate reuse itself
 *                          rather than assume "no active sessions" here.
 */
@Experimental
class DirectWorkerProcess(
    val id: String,
    private[direct] val artifacts: WorkerArtifacts,
    val connection: WorkerConnection,
    val gracefulTimeoutMs: Long,
    protected val logger: WorkerLogger = WorkerLogger.NoOp,
    private[direct] val onLastSessionReleased: DirectWorkerProcess => Unit = _ => ())
  extends AutoCloseable {

  // The ref-count drives worker teardown: when the last session releases,
  // the dispatcher's `onLastSessionReleased` callback decides what to do
  // with the worker. Today that callback terminates the worker (no reuse);
  // with pooling it will return the worker to an idle pool subject to
  // capacity / timeout.
  // TODO: Idle timeout tracking and concurrent session capacity.

  private val activeSessionCount = new AtomicInteger(0)
  private val closed = new AtomicBoolean(false)

  /** The OS process handle for this worker. */
  def process: Process = artifacts.process

  /** The UDS socket path used by this worker. */
  def socketPath: String = artifacts.socketPath

  /** Path to the merged stdout/stderr log for this worker. */
  def outputFile: Path = artifacts.outputFile

  /** Number of sessions currently using this worker. */
  def activeSessions: Int = activeSessionCount.get()

  /** Increments the active session count. */
  def acquireSession(): Unit = activeSessionCount.incrementAndGet()

  /**
   * Decrements the active session count. Fires
   * [[onLastSessionReleased]] when the count transitions to zero so the
   * dispatcher can apply its reuse policy. Logs a warning and resets to
   * zero if the count goes negative, which would indicate an unbalanced
   * acquire/release (a bug we want to surface rather than paper over,
   * especially once pooling consumes this count).
   */
  def releaseSession(): Unit = {
    val c = activeSessionCount.decrementAndGet()
    if (c < 0) {
      logger.warn(
        s"releaseSession called without a matching acquireSession (count=$c)")
      activeSessionCount.set(0)
    } else if (c == 0) {
      // Swallow callback errors so a misbehaving dispatcher cannot turn
      // `session.close()` into an exception -- close must always succeed
      // from the caller's perspective. The dispatcher callback has its
      // own error handling for the underlying worker teardown.
      try onLastSessionReleased(this) catch {
        case NonFatal(e) =>
          logger.warn(s"onLastSessionReleased callback failed for worker $id", e)
      }
    }
  }

  /** Returns true if the OS process is running and the connection is usable. */
  def isAlive: Boolean = process.isAlive && connection.isActive

  /**
   * Shuts down the connection, attempts a graceful SIGTERM, then disposes
   * the worker artifacts (forced kill + reap + file cleanup).
   *
   * Idempotent: only the first call performs teardown; subsequent calls
   * are no-ops so the dispatcher's close path and the createSession error
   * path can both invoke close without double-releasing resources.
   *
   * If the graceful SIGTERM wait expires (or is interrupted), we fall
   * through to [[WorkerArtifacts.close]], which issues SIGKILL and reaps.
   * If the process has already exited voluntarily, artifacts.close()'s
   * kill step is a no-op and we just clean up the files.
   */
  override def close(): Unit = {
    if (!closed.compareAndSet(false, true)) return

    try {
      connection.close()
    } catch {
      case NonFatal(e) =>
        logger.warn(s"Error closing connection to worker at $socketPath", e)
    }

    if (process.isAlive) {
      process.destroy() // SIGTERM
      try {
        // Whether the wait returns true (clean exit) or false (timeout),
        // we fall through to artifacts.close(); the helper's SIGKILL step
        // is a no-op on an already-dead process.
        process.waitFor(gracefulTimeoutMs, TimeUnit.MILLISECONDS)
      } catch {
        case _: InterruptedException =>
          Thread.currentThread().interrupt()
      }
    }

    artifacts.close()
  }
}

/**
 * Closeable bundle whose [[close]] always does a forced kill (SIGKILL +
 * reap) followed by socket-file and output-log cleanup. Graceful SIGTERM
 * is the responsibility of higher layers that know the worker was actually
 * running (see [[DirectWorkerProcess#close]]).
 *
 * Holds the per-worker OS-level resources that are born together at spawn
 * time -- the child [[Process]], its UDS socket file, and the merged
 * stdout/stderr log file -- so the happy-path teardown and the spawn-
 * failure teardown (before a `DirectWorkerProcess` wrapper even exists)
 * share one dispose implementation. Callers should not dispose the
 * individual fields directly.
 *
 * On the spawn-failure path the worker may never have installed signal
 * handlers; jumping straight to SIGKILL avoids paying a graceful-timeout
 * window on a process that wasn't listening anyway.
 */
private[direct] final class WorkerArtifacts(
    val process: Process,
    val socketPath: String,
    val outputFile: Path,
    private[this] val logger: WorkerLogger) extends AutoCloseable {

  private[this] val closed = new AtomicBoolean(false)

  /**
   * Idempotently tears down the three resources in a fixed order:
   *   1. SIGKILL the process and wait for the kernel to reap it.
   *   2. Delete the socket file if it was created.
   *   3. Delete the output (stdout/stderr) log file.
   *
   * Each step is guarded so a failure in one does not prevent the next.
   * Subsequent calls return immediately.
   */
  override def close(): Unit = {
    if (!closed.compareAndSet(false, true)) return

    DirectWorkerDispatcher.destroyForciblyAndReap(
      process, logger, s"worker artifacts $socketPath")

    try Files.deleteIfExists(new File(socketPath).toPath) catch {
      case NonFatal(e) =>
        logger.warn(s"Error cleaning up socket file $socketPath", e)
    }

    try Files.deleteIfExists(outputFile) catch {
      case NonFatal(e) =>
        logger.warn(s"Error cleaning up worker output file $outputFile", e)
    }
  }
}
